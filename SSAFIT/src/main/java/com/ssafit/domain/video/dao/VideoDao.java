package com.ssafit.domain.video.dao;

import java.util.List;

import org.apache.ibatis.annotations.Param;

import com.ssafit.domain.video.entity.Video;

public interface VideoDao {

    // ============================================================
    // C: 영상 등록
    // ============================================================
    int insertVideo(Video video);


    // ============================================================
    // R: 영상 단건 조회 (id 기준)
    // ============================================================
    Video selectVideoById(Long id);


    // ============================================================
    // R: 전체 영상 목록 조회 (삭제되지 않은 것만)
    // ============================================================
    List<Video> selectAllVisibleVideos();


    // ============================================================
    // R: 관리자용 전체 영상 조회 (삭제 포함 가능)
    // ============================================================
    List<Video> selectAllVideos();
   
    
    // ============================================================
    // R: ID 목록으로 조회
    // ============================================================
    List<Video> selectByIds(@Param("ids") List<Long> ids);


    // ============================================================
    // U: 영상 정보 수정
    // ============================================================
    int updateVideo(Video video);


    // ============================================================
    // D: 영상 삭제 (Soft Delete)
    // ============================================================
    int deleteVideo(Long id);


    // ============================================================
    // ETC: 조회수 증가
    // ============================================================
    int increaseViewCnt(Long id);

}
